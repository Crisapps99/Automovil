package org.example;

public class Datos {

    public String marca;
    public String modelo;
    public int anio;
    public double motor;
    public int tipoC;
    public int tipoA;
    public int puertas;
    public int asientos;
    public int velocidad;
    public int color;
    public int velocidadA;

    //Constructor
    Datos(String marca, String modelo, int anio, double motor, int tipoC, int tipoA, int puertas,
          int asientos, int velocidad, int color, int velocidadA){

        this.marca=marca;
        this.modelo=modelo;
        this.anio=anio;
        this.motor=motor;
        this.tipoC=tipoC;
        this.tipoA=tipoA;
        this.puertas=puertas;
        this.asientos=asientos;
        this.velocidad=velocidad;
        this.color=color;
        this.velocidadA=velocidadA;
    }
    //metodos


}
